// Given a character check if its a vowel or not using Switch case.
let ch='A';
switch (ch){
    case 'a':
    case 'e':
    case 'i':
    case 'o':
    case 'u':
    case 'e':
    case 'I':
    case 'O':
    case 'U':
        console.log("It is a vowel");
        break;
    default:
        console.log("consonants");
        break;

}