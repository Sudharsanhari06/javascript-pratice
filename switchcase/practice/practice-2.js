// Grade classifier:

let mark=50;
switch (true){
    case (mark>=75):
        console.log("Grade A");
        break;
    case (mark>=60):
        console.log("Grade B");
        break;
    case (mark>=45):
        console.log("Grade C");
        break;
    default:
        console.log("You didn't get grade A, B or C grade");
        break;
}