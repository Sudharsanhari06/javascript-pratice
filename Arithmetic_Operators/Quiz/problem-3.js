/* Medium question
You are given a number A i.e. the temperature in Celcius. Write a program to convert this into Fahrenheit and Kelvin
To calculate the Fahrenheit from the given Celsius is celsius*9/5+32
To calculate the Kelvin from the given Celsius is celsius+273.15 */


let celsius=35;
let fahrenheit=(celsius*9/5)+32;
let kelvin=celsius+273.15;
console.log("Fahrenheit from the given Celsius:"+fahrenheit);
console.log("Kelvin from the given Celsius:"+kelvin);